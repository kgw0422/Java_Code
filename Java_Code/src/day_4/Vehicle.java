package day_4;

public class Vehicle {
	
	private String nake;
	private String model;
	private int price;
	
	
	public String getNake() {
		return nake;
	}
	public void setNake(String nake) {
		this.nake = nake;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
